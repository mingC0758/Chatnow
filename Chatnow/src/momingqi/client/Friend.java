package momingqi.client;

public class Friend
{
	public String id;
	public String nickname;
	public String photo;
	public boolean online;
	
	public Friend(String id, String nickname, String photo)
	{
		super();
		this.id = id;
		this.nickname = nickname;
		this.photo = photo;
		this.online = false;
	}
	
	
}
